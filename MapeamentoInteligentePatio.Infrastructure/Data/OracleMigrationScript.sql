
-- Script de criação das tabelas para Oracle baseado no EF Core

CREATE TABLE FILIAIS (
    ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    NOME VARCHAR2(100),
    ENDERECO VARCHAR2(255)
);

CREATE TABLE MOTOS (
    ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    MODELO VARCHAR2(100),
    PLACA VARCHAR2(20),
    STATUS VARCHAR2(50),
    LOCALIZACAO VARCHAR2(100),
    FILIALID NUMBER,
    CONSTRAINT FK_FILIAL FOREIGN KEY (FILIALID) REFERENCES FILIAIS(ID)
);
